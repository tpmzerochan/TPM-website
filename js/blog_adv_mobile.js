// JQuery Document
$(function () {
	$(window).resize(function(){
		var current_width = $(window).width();
		if(current_width < 768){
		$("#blog_adv1").hide();
		$("#blog_adv1m").show();
		}
		if(current_width > 768){
		$("#blog_adv1").show();
		$("#blog_adv1m").hide();
		}
	});
	$(window).ready(function(){
		var current_width = $(window).width();
		if(current_width < 768){
		$("#blog_adv1").hide();
		$("#blog_adv1m").show();
		}
		if(current_width > 768){
		$("#blog_adv1").show();
		$("#blog_adv1m").hide();
		}
	});
});

